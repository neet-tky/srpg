class MAP(object):
    SIZE = (640, 480)
    def __init__(self, map_data):
        self.map = map_data
        self.row = len(self.map)
        self.col = len(self.map[0])
        self.imgs = [None] * 256

        self.msize = 32
        self.running = True

    def __call__(self):
        self.drawinit()

        while self.running:
            self.draw()
            self.drawupdate()
            self.events()

    def drawinit(self):
        pygame.init()
        self.screen = pygame.display.set_mode(self.SIZE)

        self.imgs[0] = self.load_img("grass.png")
        self.imgs[1] = self.load_img("water.png")

    def draw(self):
        for y in range(self.row):
            for x in range(self.col):
                self.screen.blit(self.imgs[self.map[y][x]], (x * self.msize, y * self.msize))

    def load_img(self, file):
        return pygame.image.load(file).convert()

    def drawupdate(self):
        pygame.display.update()

    def events(self):
        for event in pygame.event.get():
            if event.type == QUIT:
                self.running = False
                pygame.quit()
                sys.exit()

